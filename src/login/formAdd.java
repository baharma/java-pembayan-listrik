    private void onFocus(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_onFocus
        int a,b,c,d;
        try {
        a = Integer.parseInt(jTextField7.getText());
        b = Integer.parseInt(jTextField8.getText());
        c = Integer.parseInt(jTextField9.getText());
        d = (b-a)*c;
        jTextField11.setText(Integer.toString(d));
        }
        catch (NumberFormatException e)
        {
           a = 0;
           b = 0;
           c = 0;
           d = 0;
        }
    }//GEN-LAST:event_onFocus

