    private void nofaktur()
    {
       kon = koneksi.koneksiDb();
       try {
            Statement stt = kon.createStatement();
            String sql="select * from pembayaran order by noFaktur desc";
            st=kon.createStatement();
            rs=st.executeQuery(sql);
            if (rs.next()) {
                String nofak = rs.getString("noFaktur").substring(1);
                String AN = "" + (Integer.parseInt(nofak) + 1);
                String Nol = "";

                if(AN.length()==1)
                {Nol = "000";}
                else if(AN.length()==2)
                {Nol = "00";}
                else if(AN.length()==3)
                {Nol = "0";}
                else if(AN.length()==4)
                {Nol = "";}

               jTextField5.setText("F" + Nol + AN);
            } else {
               jTextField5.setText("F0001");
            }

           }catch(Exception e){
           JOptionPane.showMessageDialog(null, e);
           }
     }
    
