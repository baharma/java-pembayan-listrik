    public void tampil(){
        kon = koneksi.koneksiDb();
        try {
        
        Statement stt = kon.createStatement();
        String sql = "SELECT `noFaktur` FROM `pembayaran` WHERE `IsDelete`=0 AND `IsPay`='Tidak'";
        rs = stt.executeQuery(sql);                
        
        while(rs.next()){
            Object[] ob = new Object[3];
            ob[0] = rs.getString(1);
            
            jComboBox1.addItem((String) ob[0]);
        }
        rs.close(); stt.close();
         
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        
        
    }
